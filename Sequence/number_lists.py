# Basic operations on sequence

even = [2, 4, 6, 8]
odd = [1, 3, 5, 7, 9]

# min max
print(min(even))
print(max(even))
print(min(odd))
print(max(odd)) 
print()

# len 序列長度
print(len(even))
print(len(odd))
print()

# count 求 x 在序列 Y 中出現幾次（Y包含幾個x）
name = "Mississipi"
print(name.count("s"))
print(name.count("ss"))
print(name.count("issi"))
print()